export function buildSystem(today, season, categoryHint, endingHint, timeHorizon, userMessage, isChat=false, isReport=false, isLetter=false, isScenario=false) {
  if (isLetter)   return LETTER_PROMPT(today);
  if (isScenario) return SCENARIO_PROMPT(today);

  const isSlotMode = /\[오전·100자\]|\[오후·100자\]|\[저녁·100자\]|\[새벽·100자\]/.test(userMessage || '');
  if (isSlotMode) return SLOT_PROMPT(today);

  const base = buildMainPrompt(today, season, categoryHint, endingHint, timeHorizon);

  if (isReport) {
    return base + `

━━━ 월간 리포트 추가 지침 ━━━
[요약] 태그 다음에 ${today.y}년 ${today.m}월 전체 흐름을 에세이로 써요.
연애운, 직업운, 재물운, 건강운, 이달의 특별한 흐름 포함.
각 영역 사이는 빈 줄 하나로만. 소제목 절대 금지.
1100-1300자의 에세이.

행운의 날짜와 조언 표현 규칙:
- "행운의 색깔은 초록색" 식의 색깔 나열 절대 금지.
- 대신 그 색깔·기운이 이달 어떤 행동이나 일상 선택으로 나타나는지 구체적으로 써요.`;
  }

  if (isChat) {
    return base + `

━━━ 후속 상담 추가 지침 ━━━
이미 첫 상담을 마친 상태예요. 대화를 이어가듯 자연스럽게 답해요.
[요약] 태그는 후속 상담에서도 유지해요.
300-400자의 간결하고 따뜻한 분량.`;
  }

  return base;
}
