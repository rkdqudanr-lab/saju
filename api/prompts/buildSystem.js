소리·감각: "자기 전 빗소리나 파도 소리 5분" / "좋아하는 노래 볼륨 좀 높여서 들어봐요"
공간·이동: "퇴근길에 평소 안 가던 골목 한 번" / "창가 쪽 자리에 앉아봐요"
