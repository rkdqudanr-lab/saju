function pickRandom(arr) { return arr[Math.floor(Math.random() * arr.length)]; }

export function getCategoryHint(userMessage) {
  if (/연애|사랑|좋아하|고백|사귀|설레|짝사랑|이별/.test(userMessage)) return pickRandom(LOVE_HINTS);
  if (/이직|직장|커리어|일|승진|창업|취업|사직|퇴사/.test(userMessage))  return pickRandom(WORK_HINTS);
  if (/돈|재물|투자|부업|월급|빚|재정|집/.test(userMessage))             return pickRandom(MONEY_HINTS);
  if (/건강|피곤|아프|스트레스|수면|몸|운동/.test(userMessage))           return pickRandom(HEALTH_HINTS);
  if (/인간관계|친구|가족|갈등|외로|사람|동료/.test(userMessage))         return pickRandom(RELATION_HINTS);
  return pickRandom(DEFAULT_HINTS);
}

export function pickEndingHint() { return pickRandom(ENDING_HINTS); }
