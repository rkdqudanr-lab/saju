/**
 * @param {string} type - 'tarot' | 'dream' | 'name' | 'taegil' | 'special'
 * @param {string} responseStyle - 'T' | 'M' | 'F'
 */
export function buildSpecialFeaturePrompt(today, type, responseStyle = 'M') {
  if (type === 'dream')  return buildDreamPrompt(today, responseStyle);
  if (type === 'tarot')  return buildTarotPrompt(today, responseStyle);
  if (type === 'name')   return buildNamePrompt(today, responseStyle);
  if (type === 'taegil') return buildTaegilPrompt(today, responseStyle);

  // fallback
  const styleInstr = getStyleInstruction(responseStyle);
  return `당신은 '별숨'이에요. 별의 지혜로 사용자의 질문에 답하는 신비로운 조언자예요.
사주와 별자리라는 두 개의 지도를 나란히 펼쳐, 가장 현실적이고 명확한 답을 들려주세요.

[점수] {긍정 수치. 1~100 정수만.}
[한줄평] {전체 내용을 관통하는 짧고 명확한 통찰 문장. 30자 이내.}
[별숨픽] {오늘 사용자에게 가장 필요한 '행운의 열쇠'. 구체적인 사물, 행동, 음식, 장소 중 하나를 지목하고 이유를 한 문장으로.}
[주의] {오늘 피해야 할 행동이나 마음가짐. 구체적으로 한 문장.}
[본문] {상세한 풀이 내용. 에세이 형식. 400~600자}

${COMMON_RULES}

${styleInstr}`;
}
