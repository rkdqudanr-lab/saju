/**
 * 게이미피케이션 일일 운세 시스템 프롬프트 빌더
 * @param {{ solar: string, lunar: string, jeolgi: string, timeSlot: string, y: number, m: number, d: number }} today
 * @param {string} season
 * @param {'T'|'M'|'F'} responseStyle
 * @param {'남'|'여'|null} gender - 사용자 성별 (아이템 풀 분기용)
 * @returns {string}
 */
export function buildDailyHoroscopeGamified(today, season, responseStyle = 'M', gender = null) {
  const randomIntent = DIARY_INTENTS[Math.floor(Math.random() * DIARY_INTENTS.length)];
  if (responseStyle === 'T') return buildPromptT(today, season, randomIntent, gender);
  if (responseStyle === 'F') return buildPromptF(today, season, randomIntent, gender);
  return buildPromptM(today, season, randomIntent, gender);
}

/**
 * sajuInfo / astroInfo 기반 API (getDailyHoroscopePrompt 형태로도 호출 가능)
 * @param {object|null} sajuInfo  - 사주 정보 (현재는 today/season으로 대체)
 * @param {object|null} astroInfo - 점성술 정보
 * @param {'T'|'M'|'F'} responseStyle
 * @returns {string}
 */
export const getDailyHoroscopePrompt = (sajuInfo, astroInfo, responseStyle = 'M') => {
  // today/season 정보가 sajuInfo 안에 담겨 오거나 없을 경우 기본값 사용
  const today = sajuInfo?.today ?? {
    solar: '', lunar: '', jeolgi: '', timeSlot: 'afternoon', y: 0, m: 0, d: 0
  };
  const season = sajuInfo?.season ?? '';
  return buildDailyHoroscopeGamified(today, season, responseStyle);
};
