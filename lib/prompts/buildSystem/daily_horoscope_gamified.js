// ── 날짜 시드 기반 후보 선별 ───────────────────────────────────
// 100개 풀을 8구간으로 분할 → 날짜마다 다른 구간 + 내부 오프셋으로 8개 추출
function pickDailyCandidates(pool, seed, count = 8) {
  const result = [];
  const span = Math.floor(pool.length / count);
  for (let i = 0; i < count; i++) {
    const base = i * span;
    const offset = (seed * (i + 1) * 7 + seed % (span || 1)) % span;
    const idx = (base + offset) % pool.length;
    const item = pool[idx];
    if (!result.includes(item)) result.push(item);
  }
  return result;
}

