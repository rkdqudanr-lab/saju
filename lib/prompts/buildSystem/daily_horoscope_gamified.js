/**
 * 게이미피케이션 일일 운세 시스템 프롬프트 빌더
 */
export function buildDailyHoroscopeGamified(today, season, responseStyle = 'M', gender = null, lifeStage = 'free') {
  const hour = typeof today.hour === 'number' ? today.hour : new Date().getHours();
  const randomIntent = pickDiaryIntent(hour);
  if (responseStyle === 'T') return buildPromptT(today, season, randomIntent, gender, lifeStage);
  if (responseStyle === 'F') return buildPromptF(today, season, randomIntent, gender, lifeStage);
  return buildPromptM(today, season, randomIntent, gender, lifeStage);
}

/**
 * sajuInfo / astroInfo 기반 호환 API
 */
export const getDailyHoroscopePrompt = (sajuInfo, astroInfo, responseStyle = 'M') => {
  const today = sajuInfo?.today ?? {
    solar: '', lunar: '', jeolgi: '', timeSlot: 'afternoon', y: 0, m: 0, d: 0
  };
  const season = sajuInfo?.season ?? '';
  return buildDailyHoroscopeGamified(today, season, responseStyle);
};
